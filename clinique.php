	l'aggraver et au contraire un sentiment de sécurité de bien être peut le diminuer.</p>
	<h2>La bradykinésie et akinésie </h2>
	<p class=alinea>La bradykinésie est déterminée par la lenteur des mouvements volontaires. Si ces mouvements ne peuvent pas être effectués, on parle alors d'akinésie. 
	Cependant le degré diffère selon le mouvement, on en considère deux sortes : ceux qui demandent de la précision et les semi automatiques (comme la marche ou l'écriture).
	Une des particularités de l'akinésie reste la variabilité de difficulté d'effectuer un mouvement. Parfois cela va paraître un jeu d'enfant pour le patient alors qu'il va sembler insurmontable quelques temps après. 
	Cette variation est dû à l'état du patient au moment où il fait le mouvement, selon son état émotionnel, sa vigilance, les activités faites en parallèle, les stimulis visuels et auditif qui peuvent faciliter ou perturber ce que le patient fait.
	Quand on parle des mouvements, on observe bien souvent une démarche plus lente et des tous petits pas, le visage n'est plus aussi actif qu'auparavant, les mouvements habituels deviennent plus compliqués (s'habiller, prendre soin de soi).</p>
	<h2>La rigidité ou hypertonie</h2>
	<p class=alinea>Elle est définie par lune augmentation du tonus musculaire, le patient ressent une tension plus présente d'un côté et provoque une douleur musculaire. Les mouvements effectués par le patient deviennent passifs et saccadés, il n'y a plus d'aisance naturelle. 
	Elle concerne l'ensemble des muscles mais se concentre autour de la colonne vertébrale et des articulations. 
	Ce symptôme est lui aussi sensible au stress ou à l'émotion du patient.</p>
