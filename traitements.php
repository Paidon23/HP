mais permets une meilleure vie du patient en améliorant ses problèmes moteurs. Comme toute neurochirurgie, cela reste une option risquée qui peut 
altérer des fonctions cognitives à la base non touchées par la maladie.</p>
<p class=alinea>N'ayant aujourd'hui encore pas de traitements pour guérir la maladie, les objectifs sont de soulager les symptomes du patients et de ralentir la maladie. 
