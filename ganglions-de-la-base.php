		<p class=legend width=300>Figure 3 - Les différentes structures composant les ganglions de la base<sup><a href="references.php?ref=36#36">[36]</a></sup>.
		</p></div><h2>Description des ganglions de la base</h2>
		<p class=alinea>Les ganglions de la base désignent un réseau de 
		structures profondes du cerveau interconnectées, parmi lesquelles<sup><a href="references.php?ref=29#29">[29]</a>, <a href="references.php?ref=30#30">[30]</a>, 
		<a href="references.php?ref=35#35">[35]</a>, <a href="references.php?ref=36#36">[36]</a></sup>: </p>
		<ul class=normal><li class=normal>Le <b>noyau caudé</b> ;
		<li class=normal>Le <b>putamen</b>, séparé du précédent par des fibres appelées capsules internes ;</li>
		<li class=normal>Le <b>globus pallidus</b>, ou pallidum, séparé en deux parties, interne et externe ;</li>
		<li class=normal>Le <b>noyau subthalamique</b> ;</li>
		<li class=normal>La <b>substance noire</b>, ou substantia nigra, divisée en parties compacte et réticulée.</li></ul>
		<p class=alinea>Toutes ces structures sont paires (une pour chaque hémisphère) dans le cerveau. Selon les sources,
		on rajoute parfois à cette liste le <b>noyau accumbens</b>, faisant partie du striatum ventral avec le bulbe olfactif<sup><a href="references.php?ref=35#35">[35]</a></sup>. 
		L'ensemble formé du noyau caudé et du putamen est désigné sous le nom de striatum (ou corps strié), et parfois
		avec la précision « striatum dorsal » si l'on prend en compte sa partie ventrale. D'un autre côté, on trouve
		aussi la dénomination « noyau lenticulaire » comme étant l'ensemble formé du putamen et du pallidum. Enfin,
		on peut également trouver le nom de « néostriatum » pour le noyau caudé et le putamen, et « paléostriatum »
		pour le pallidum, ces noms mettant cette fois-ci en avant les structures les plus récentes d'un point de vue
		phylogénétique<sup><a href="references.php?ref=38#38">[38]</a></sup>.</p>
		
		<h2>Le système moteur</h2>
		<div class=floatright><img src="images/SéquenceMotrice.png" alt="Relations ganglions de la base" width=525 height=255 />
		<p class=legend style="width:525px;">Figure 4 - Schéma de la séquence motrice : dans un premier temps, le tennisman initie le mouvement :
		il réfléchit à l'endroit où il doit viser pour frapper la balle et qu'elle atteigne un endroit spécifique sur le terrain.
		Ensuite, c'est la planification, visant à créer le programme pour réaliser le mouvement voulu. Enfin, la séquence motrice
		s'exécute<sup><a href="references.php?ref=40#40">[40]</a></sup>.</p></div>
		<p class=alinea>La production d'un mouvement met en jeu un ensemble de structures et de réseaux cérébraux.
		On peut décomposer la séquence aboutissant au mouvement en trois opérations (cf. figure 4):
		<ul class=normal><li class=normal>L'<b>initiation</b> du mouvement est réalisée par les lobes frontal et préfrontal,
		qui intègrent des informations, notamment sensorielles, provenant de différentes aires cérébrales.
		Lors de cette étape, on choisit une réponse motrice adaptée en fonction de la situation : on
		décide ce que l'on souhaite faire ;</li>
		<li class=normal>La <b>planification</b> du mouvement fait intervenir l'aire prémotrice et l'aire motrice supplémentaire.
		Lors de cette étape, le cerveau réalise la programmation de la séquence motrice nécessaire
		pour effectuer un mouvement d'un point de l'espace à un autre, selon un angle et une vitesse précis ;</li>
		<li class=normal>Enfin, l'<b>exécution</b> du mouvement est réalisée par le cortex moteur primaire, via la voie pyramidale,
		faisant transiter l'information vers le tronc cérébral, la moelle épinière, puis les 
		muscles sollicités<sup><a href="references.php?ref=40#40">[40]</a></sup>.</li></ul></p>
		<p class=alinea>Deux structures cérébrales interviennent sur cette voie en la régulant : les <b>ganglions de la base</b>, lors de l'initiation,
		et le <b>cervelet</b>, lors de la planification. Les ganglions de la base ont pour rôle de supprimer les mouvements non-désirés, 
		et de préparer les neurones de la circuiterie motrice à la production du mouvement : ils permettent donc d'enclencher
		et de maintenir le mouvement.
		Le cervelet, quant à lui, a pour rôle de détecter la différence entre le mouvement voulu et le mouvement effectivement
		produit. Ainsi, il peut permettre de réduire ces « erreurs motrices » sur le moment. A long terme, le cervelet
		agit ainsi comme une structure d'apprentissage moteur<sup><a href="references.php?ref=30#30">[30]</a></sup>. 
		
