		<h2>Rôle moteur des ganglions de la base</h2>
		<p class=alinea>Certains rôles des ganglions de la base restent méconnus. Toutefois, leur intervention dans l'activité motrice
		volontaire est certaine, notamment car leur dégradation est liée à des pathologies caractérisées
		par des dysfonctionnements du système moteur (comme les maladies de Parkinson ou Huntington)<sup><a href="references.php?ref=39#39">[39]</a></sup>.
		Actifs avant et pendant les mouvements, les ganglions de la base
		permettent le déclenchement, l'amorçage normal et « harmonieux » de mouvements volontaires. 
		Les mouvements en question sont des processus sur-appris, automatisés, que les ganglions de la base
		adptent au contexte, à la situation dans laquelle ils sont employés.</p>
		<p class=alinea>La quasi-totalité du cortex cérébral envoie des informations
		vers le <b>striatum</b>, qui forme le point d'entrée des ganglions de la base.
		En effet, les noyaux du striatum contiennent des neurones appelés 
		<b>neurones épineux moyens</b>, dont les larges	arbres dendritiques 
		leur permettent d'intégrer un grand nombre d'informations provenant de multiples zones cérébrales.
		Le noyau caudé et le putamen ne reçoivent pas les mêmes informations : 
		ainsi, on remarque que le noyau caudé s'active juste avant un mouvement de
		l'œil, tandis que le putamen s'active plutôt avant un mouvement du reste du corps.
		Les informations transitent ensuitent en direction du <b>pallidum</b>, qui reçoit les
		afférences du putamen, et de la partie réticulée de la <b>substance noire</b>, qui reçoit les afférences du noyau caudé.
		Ces deux noyaux sont les noyaux de sortie de l'information des ganglions de la base.
		Ils projettent vers le <b>thalamus</b> et le colliculus supérieur (responsable des saccades oculaires). Les informations
		reçues par le thalamus sont ensuite redirigées vers le <b>cortex frontal</b><sup><a href="references.php?ref=30#30">[30]</a>,<a href="references.php?ref=40#40">[40]</a></sup>.</p>
		<p class=alinea>Le système des ganglions de la base fonctionne selon un ensemble d'inhibitions
		et d'activations. Le globus pallidus et la substance noire sont toujours actifs de façon
		tonique au repos (i.e., lorsqu'aucun mouvement volontaire n'est enclenché). L'activité
		du globus pallidus inhibe celle du thalamus, ce qui permet d'éviter de déclencher un mouvement
		non-désiré (cf. figure 5, repos). A l'inverse, lorsqu'un mouvement est initié par le cortex frontal, le putamen
		est activé, ce qui permet d'inhiber le globus pallidus interne, et donc de relâcher le thalamus qui
		peut s'exprimer et lancer le mouvement. La substance noire compacte permet de renforcer ce
		circuit en cas de mouvement, en activant les neurones ayant des récepteurs dopaminergiques D1 (cf. figure 5, mouvement).</p>
		<p class=alinea>Les neurones possédant des récepteurs D2 font partie d'une voie indirecte, visant à modérer
		l'activation de la voie principale. Ces neurones inhibent la partie externe du globus pallidus, 
		permettant au noyau subthalamique d'activer le globus pallidus interne, et donc de réguler les
		informations envoyées au thalamus. Cette voie agit comme un « frein », contrôlé par l'action
		inhibitrice de la substance noire compacte. Le système entier est donc modulé par ces deux voies
		qui agissent sur le thalamus. Cela permet une gestion fine de l'activation de la voie motrice 
		principale en assurant (voie directe) ou en inhibant (voie indirecte) le mouvement.
		Le thalamus projette ensuite sur l'aire motrice supplémentaire : on suppose que le
		mouvement se déclenche si celle-ci est activée par le thalamus au-delà d'un certain
		seuil<sup><a href="references.php?ref=30#30">[30]</a>,<a href="references.php?ref=39#39">[39]</a>,<a href="references.php?ref=40#40">[40]</a></sup>.
		<p class=alinea>Ce sont les pathologies impliquant un dysfonctionnement des ganglions de la base qui ont permis
		d'éclairer le rôle de cette boucle : nous verrons par la suite que la pauvreté de mouvements dans la
		maladie de Parkinson est due à une sur-activation de la voie indirecte, tandis que les mouvements
		incontrôlables dans la maladie de Huntington trouve son origine dans une sur-activation de la 
		voie directe<sup><a href="references.php?ref=39#39">[39]</a></sup>.</p>
		
		<h2>Autres rôles des ganglions de la base</h2>
		<p class=alinea>Le cortex moteur n'est pas la seule région du cerveau à envoyer des projections en direction du striatum :
		en réalité, quasiment toutes les régions du néocortex y communiquent des informations. On suppose ainsi
		des fonctions des ganglions de la base dans :</p>
		<ul class=normal><li class=normal>Des processus cognitifs, comme la planification, la mémoire de travail, l'attention ;</li>
		<li class=normal>Des processus émotionnels, comme le comportement ou la motivation.</li></ul>
		<p class=alinea>Il est à noter que les maladies de Huntington et de Parkinson ne sont pas les seules a être liées
		aux ganglions de la base. Dans la littérature, on y associe notamment le syndrome de la Tourette,
		ou les Troubles Obsessionnels Compulsifs<sup><a href="references.php?ref=30#30">[30]</a>,<a href="references.php?ref=42#42">[42]</a></sup>.</p>
		
