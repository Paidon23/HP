		<p class=intro>Les maladies de Parkinson et de Huntington sont toutes deux liées à des lésions
		des ganglions de la base. Mais quelles sont ces structures et à quoi servent-elles ?</p>
		
		<p class=alinea>Le système nerveux central, formé de l'encéphale et de la moelle épinière,
		est divisible en substance blanche et en substance grise. Ces deux types de régions doivent leur nom à leurs
		propriétés anatomiques, la substance grise étant visuellement plus sombre que la substance blanche
		lors de la découpe du cerveau. La substance grise correspond
		aux noyaux des neurones, et la substance blanche à leurs axones. Dans l'encéphale,
		la substance grise a pour particularité d'être majoritairement répartie en périphérie,
		sur l'écorce du cerveau (d'où le nom de cortex cérébral). A l'inverse, la substance blanche 
		est localisée de façon plus centrale. On retrouve cependant des noyaux de 
		substance grise en profondeur du cerveau : les ganglions de la base en font partie<sup><a href="references.php?ref=30#30">[30]</a></sup>.</p>

