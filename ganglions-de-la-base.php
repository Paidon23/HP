		<h2>Rôle moteur des ganglions de la base</h2>
		<p class=alinea>Certains rôles des ganglions de la base restent méconnus. Toutefois, leur intervention dans l'activité motrice
		volontaire est certaine, notamment car leur dégradation est liée à des pathologies caractérisées
		par des dysfonctionnements du système moteur (comme Parkinson ou Huntington). 
		A la surface du cerveau, l'information motrice volontaire est déclenchée par le cortex moteur primaire, qui,
		via la voie pyramidale, transmet l'information aux muscles. En même temps, le cortex moteur envoie également
		des informations en direction des ganglions de la base, plus particulièrement au niveau du striatum.
		Les informations en entrée sont partagées : ainsi, le noyau caudé et le putamen recoivent chacun des informations 
		de zones différentes du cortex. 
		Le striatum contient des neurones appelés neurones épineux moyens, dont les larges
		arbres dendritiques leur permettent d'intégrer un grand nombre d'informations provenant de multiples zones
		cérébrales. Ensuite, les informations transitent en direction du pallidum et de la pars reticulata
		de la substance noire. Ces deux noyaux sont les noyaux de sortie de l'information des ganglions de la base.
		Ils projettent vers le thalamus et le colliculus supérieur (responsable des saccades oculaires). Les informations
		reçues par le thalamus sont ensuite redirigées vers le cortex moteur primaire.</p>
		<p class=alinea>Ce sont les pathologies impliquant un dysfonctionnement des ganglions de la base qui ont permis
		d'éclairer sur le rôle de cette boucle. Les ganglions de la base régulent l'activité des neurones
		moteurs du cortex, et ainsi influencent la motricité. Actifs avant et pendant les mouvements, ils
		permettent le déclenchement, l'amorçage normal et « harmonieux » de mouvements volontaires. 
		Les mouvements en questions sont des processus sur-appris, automatisés, que les ganglions de la base
		vont adapter au contexte, à la situation dans laquelle ils sont employés.</p>
		
		<h2>Autres rôles des ganglions de la base</h2>
		Le cortex moteur n'est pas la seule région du cerveau à envoyer des projections en direction du striatum :
		en réalité, quasiment toutes les régions du néocortex y communiquent des informations.
		
